export const milisecondsSecond = 1000;
export const milisecondsMinute = milisecondsSecond * 60;
export const milisecondsHour = milisecondsMinute * 60;
export const milisecondsDay = milisecondsHour * 24;
export const milisecondsMonth = milisecondsDay * 30;
export const milisecondsYear = milisecondsMonth * 12;
